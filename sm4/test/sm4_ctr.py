from cryptography.hazmat.primitives.ciphers import Cipher, algorithms, modes

data = b'\x78\xb5\xc9\xf9\xf4\xd4\xb0\x56\xa4\x71\xe3\x87\x85\xf5\xdf\x49\x2a\xda\xd9\x2e\x6e\xd0\xe5\xd6\xb5\x0e\x08\x19\x99\xda\x11\x11\x8f\xda\x0a\x83\xaf\xbb\xd9\x53\x2c\xbd\xda\xb2\xb2\xb9\xfb\xdc\x94\xd4\x0a\x02\xa4\xef\xd8\x5a\xfd\xe1\x73\x96\xbb\x85\xa8\x4a'
key = b'\xf6\x08\x5a\x28\x47\x8c\x10\x33\xb1\xd2\x8d\x16\x18\xfa\xb0\xc6'


cipher = Cipher(algorithms.SM4(key), modes.ECB())
encryptor = cipher.encryptor()

ciphertext = encryptor.update(data) + encryptor.finalize()

print("Cipher:",ciphertext.hex())
