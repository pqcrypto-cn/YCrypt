void sm3_self_check()
{
	puts("======== Test SM3 correctness using self test vector ========");

	uint8_t msg[100] = { 0 };
	uint8_t digest[32] = { 0 };
	uint8_t expected_digest[32] =
	{
		0x4B, 0x28, 0x33, 0xC1, 0x58, 0xDD, 0x41, 0x61, 0x4B, 0x76, 0xE3, 0x7F, 0x18, 0x88, 0x92, 0x43,
		0xBD, 0x6B, 0x4A, 0x74, 0x4E, 0x36, 0xDE, 0x60, 0x92, 0x0A, 0x2F, 0x89, 0xE4, 0x09, 0xC6, 0x4E,
	};
	size_t i = 0;
	size_t msg_len = 100;

	for (i = 0; i < msg_len; ++i)
	{
		msg[i] = i;
	}

	sm3(msg, msg_len, digest);

	if (memcmp(digest, expected_digest, 32) != 0)
	{
		puts("[ERROR] Digest and expected is not equal!");
		return;
	}

	puts("[SUCCESS] SM3 selft test pass!");
}

